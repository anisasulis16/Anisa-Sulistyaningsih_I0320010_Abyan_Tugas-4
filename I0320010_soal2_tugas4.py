#Soal no 2 Operator Floor

#Menginputkan dua bilangan bulat
print("MASUKKAN BILANGAN BULAT")
bilangan1 = float(input("Masukkan bilangan pertama : "))
bilangan2 = float(input("Masukkan bilangan kedua : "))

print(".........................")
print("Bilangan Bulat = ", bilangan1, ",", bilangan2)

#Memproses program
hasil_program = bilangan1 // bilangan2

#Menampilkan Hasil
print("Jadi banyaknya bilangan kedua dibagi oleh bilangan pertama sebanyak ", bilangan1, "//", bilangan2, "=", hasil_program, "kali.")